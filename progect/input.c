#include "person.h"
// Вспомогательные функции для валидации и очистки буфера ввода
int validate_name(const char *name) { // Проверка, что имя не пустое и содержит только буквы и пробелы
    if (strlen(name) == 0) return 0; // Имя не может быть пустым
    for (int i = 0; name[i]; i++) { // Проверка каждого символа в имени
        if (!isalpha(name[i]) && name[i] != ' ') { // Разрешаем только буквы и пробелы
            return 0; // Если найден недопустимый символ, возвращаем 0 (невалидно)
        }
    }
    return 1;
}

// Валидация положительного целого числа
int validate_positive_int(int value) { 
    return value > 0;
}

// Валидация положительного числа с плавающей точкой
int validate_positive_float(float value) {
    return value > 0.0f;
}

// Очистка буфера ввода после чтения числового значения, чтобы избежать проблем с последующим вводом строк
void clear_input_buffer(void) {
    int c;
    while ((c = getchar()) != '\n' && c != EOF); // Читаем и игнорируем все символы до конца строки
}