/* Задание 1: Открытие и закрытие файла */
#include <stdio.h>
#include <stdlib.h>

int main() {
    FILE *fp;

    printf("** Задание 1: Открытие и закрытие файла **\n");

    // Открытие файла в режиме записи
    fp = fopen("test.txt", "w");

    // Проверка открытия
    if (fp == NULL) {
        printf("** Ошибка: не удалось открыть файл **\n");
        return 1;
    }

    printf("** Файл успешно открыт **\n");

    // Закрытие файла
    fclose(fp);
    printf("** Файл закрыт **\n");

    return 0;
}
